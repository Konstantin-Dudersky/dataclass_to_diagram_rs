use std::fmt;

use super::super::sprites::ISprite;

impl fmt::Display for Devicons {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{:?}", self)
    }
}

impl ISprite for Devicons {
    fn export(&self) -> (String, Vec<String>) {
        let name = self.to_string();
        let include_sprite = format!("!include <tupadr3/devicons/{}>", name);
        (
            name,
            vec!["!include <tupadr3/common>".into(), include_sprite],
        )
    }
}

